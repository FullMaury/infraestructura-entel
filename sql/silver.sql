    FECHA_INICIO DATETIME,
    FECHA_FIN DATETIME,
    NUMERO NVARCHAR(12),
    IMEI NVARCHAR(18),
    RBS_UTILIZADA NVARCHAR(50),
    CELDA NVARCHAR(8),
    LATITUD DECIMAL(9,6),
    LONGITUD DECIMAL(9,6),
    DATOS_SUBIDOS BIGINT,
    DATOS_DESCARGADOS BIGINT,
    VOLUMEN_TOTAL BIGINT
);
